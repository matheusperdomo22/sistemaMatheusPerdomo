    public void beanView(McpVendas mcpvendas) {
        jTxtCodigo.setText(mcp_util.intToStr(mcpvendas.getMcpIdVenda()));
        //jFmtData.setText(mcp_util.dateToStr(mcpvendas.getMcpDataVenda()));
        jTxtTotal.setText(mcpvendas.getMcpTotal());
        jTxtFormadePagamento.setText(mcpvendas.getMcpFormaPagamento());
        jTxtObservacoes.setText(mcpvendas.getMcpObservacoes());
        jCboClientes.setSelectedItem(mcpvendas.getMcpClientes());
        jCboCupons.setSelectedItem(mcpvendas.getMcpCupons());
    }

    public McpVendas viewBean() {
        McpVendas mcpvendas = new McpVendas();
        int codigo = mcp_util.strToInt(jTxtCodigo.getText());
        mcpvendas.setMcpIdVenda(codigo);
        mcpvendas.setMcpTotal(jTxtTotal.getText());
        mcpvendas.setMcpFormaPagamento(jTxtFormadePagamento.getText());
        mcpvendas.setMcpObservacoes(jTxtObservacoes.getText());
        mcpvendas.setMcpClientes((McpClientes)jCboClientes.getSelectedItem());
        mcpvendas.setMcpCupons((McpCupons)jCboCupons.getSelectedItem());
       
        return mcpvendas;
    }

