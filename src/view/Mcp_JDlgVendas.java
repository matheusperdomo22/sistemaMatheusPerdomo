    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        if (mcp_util.pergunta("Deseja excluir esta venda?")) {
        JOptionPane.showMessageDialog(null, "Venda excluída com sucesso!");
        
        Mcp_VendasDAO mcpvendasDAO = new Mcp_VendasDAO();
        mcpvendasDAO.delete(viewBean());
        
        mcp_util.limpar(
            jTxtCodigo, 
            jFmtData, 
            jTxtFormadePagamento, 
            jTxtObservacoes, 
            jTxtTotal
        );
    }

    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        Mcp_VendasDAO mcp_vendasDAO = new Mcp_VendasDAO();
    McpVendas mcp_vendas = viewBean();
    if (incluir == true) {
        mcp_vendasDAO.insert(mcp_vendas);
    } else {
        mcp_vendasDAO.update(mcp_vendas);
    }

    mcp_util.habilitar(false, 
        jTxtCodigo, 
        jCboClientes, 
        jCboCupons, 
        jFmtData, 
        jTxtFormadePagamento, 
        jTxtObservacoes, 
        jTxtTotal, 
        jBtnConfirmar, 
        jBtnCancelar
    );
    mcp_util.habilitar(true, 
        jBtnIncluir, 
        jBtnAlterar, 
        jBtnExcluir, 
        jBtnPesquisar, 
        jBtnIncluirProd, 
        jBtnAlterarProd, 
        jBtnExcluirProd
    );

    mcp_util.limpar(
        jTxtCodigo, 
        jFmtData, 
        jTxtFormadePagamento, 
        jTxtObservacoes, 
        jTxtTotal
    );
    
    JOptionPane.showMessageDialog(this, "Venda salva com sucesso!");

    }//GEN-LAST:event_jBtnConfirmarActionPerformed

